struct UnionFind {
    vector<int> par; // par[i]:iの親の番号　(例) par[3] = 2 : 3の親が2

    UnionFind(int N) : par(N) { //最初は全てが根であるとして初期化
        for(int i = 0; i < N; i++) par[i] = i;
    }

    int root(int x) { // データxが属する木の根を再帰で得る：root(x) = {xの木の根}
        if (par[x] == x) return x;
        return par[x] = root(par[x]);
    }

    void unite(int x, int y) { // xとyの木を併合
        int rx = root(x); //xの根をrx
        int ry = root(y); //yの根をry
        if (rx == ry) return; //xとyの根が同じ(=同じ木にある)時はそのまま
        par[rx] = ry; //xとyの根が同じでない(=同じ木にない)時：xの根rxをyの根ryにつける
    }

    bool same(int x, int y) { // 2つのデータx, yが属する木が同じならtrueを返す
        int rx = root(x);
        int ry = root(y);
        return rx == ry;
    }
};


// Edge e = {weight, (node1, node2)}
using Edge = pair<int, pair<int, int>>;

int main(){
    int N, M;
    cin >> N >> M;
    vector<Edge> edges(M);
    REP(i, M){
        int u, v, w;
        cin >> u >> v >> w;
        edges[i] = Edge(w, {u, v});
    }

    // SORT
    sort(ALL(edges));

    // kruskal method
    UnionFind groups(N);
    LL res = 0;
    REP(i, M){
        Edge e = edges[i];
        auto p = e.second;

        // Loop判定．追加するエッジの両端点が同じグループに所属していないか．
        if(groups.same(p.first, p.second)){
            continue;
        }

        res += e.first;
        groups.unite(p.first, p.second);
    }

    cout << res << endl;
    return 0;
}