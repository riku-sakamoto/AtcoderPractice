struct UnionFind {
    vector<int> par; // par[i]:iの親の番号　(例) par[3] = 2 : 3の親が2

    UnionFind(int N) : par(N) { //最初は全てが根であるとして初期化
        for(int i = 0; i < N; i++) par[i] = i;
    }

    int root(int x) { // データxが属する木の根を再帰で得る：root(x) = {xの木の根}
        if (par[x] == x) return x;
        return par[x] = root(par[x]);
    }

    void unite(int x, int y) { // xとyの木を併合
        int rx = root(x); //xの根をrx
        int ry = root(y); //yの根をry
        if (rx == ry) return; //xとyの根が同じ(=同じ木にある)時はそのまま
        par[rx] = ry; //xとyの根が同じでない(=同じ木にない)時：xの根rxをyの根ryにつける
    }

    bool same(int x, int y) { // 2つのデータx, yが属する木が同じならtrueを返す
        int rx = root(x);
        int ry = root(y);
        return rx == ry;
    }
};


int main(){
    int N, M, K; cin >> N >> M >> K;
    vector<int> n_frineds(N, 0);
    UnionFind groups(N);
    REP(i, M){
        int a, b; cin >> a >> b; --a; --b;
        groups.unite(a, b);
        n_frineds[a] += 1;
        n_frineds[b] += 1;
    }

    map<int, set<int>> friends;
    REP(i, N){
        friends[groups.root(i)].insert(i);
    }

    vector<int> n_blocks(N, 0);
    REP(i, K){
        int a, b; cin >> a >> b; --a; --b;
        if(groups.same(a, b)){
            n_blocks[a] += 1;
            n_blocks[b] += 1;
        }
    }

    vector<int> answers(N);
    REP(i, N){
        auto n_f = friends[groups.root(i)].size() - 1;
        n_f -= n_frineds[i];
        n_f -= n_blocks[i];

        answers[i] = n_f;
    }

    REP(i, N){
        out(answers[i]);
    }
    return 0;
}